# üéØ –≠—Ç–∞–ª–æ–Ω Dreamspell: Kin 1 = 26.07.1987
JD_DREAMSPELL_REF = sum(gcal2jd(1987, 7, 26))

@app.get("/calculate-kin")
def calculate_kin(date: str = Query(..., description="–î–∞—Ç–∞ YYYY-MM-DD")):
    try:
        year, month, day = map(int, date.split("-"))
    except:
        return JSONResponse(status_code=400, content={"error": "–§–æ—Ä–º–∞—Ç –¥–∞—Ç—ã: YYYY-MM-DD"})

    JD = sum(gcal2jd(year, month, day))
    delta_days = JD - JD_DREAMSPELL_REF

    kin = int((delta_days % 260) + 1)
    tone = ((kin - 1) % 13) + 1
    seal_index = ((kin - 1) % 20)
    seal = SEALS_FULL[seal_index]

    return {
        "Kin": kin,
        "Tone": tone,
        "SealNumber": seal_index + 1,
        "SealName": seal["name"],
        "SealFull": f"{seal['name']} ‚Äî {seal['desc']}",
        "DeltaDays": delta_days,
        "JD_Ref": JD_DREAMSPELL_REF
    }
